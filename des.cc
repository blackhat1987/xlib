DesKey::DesKey(const void* key, size_t size)
  {
  memset(Keys, 0, sizeof(Keys));
  if(size == 0)
    {
    const char* k = (const char*)key;
    while(k[size] != '\0') ++size;
    }
  if(size > sizeof(Keys))  size = sizeof(Keys);
  memcpy(Keys, key, size);
  }

