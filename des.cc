static void doxor(uint8* source, const uint8* target, const size_t length)
  {
  for(size_t i = 0; i < length; ++i)
    {
    source[i] ^= target[i];
    }
  }

static void setbit(uint8* data, const size_t pos, const bool b)
  {
  const size_t byte_count = (pos - 1) / 8;
  const size_t bit_count = 7 - ((pos - 1) % 8);
  const uint8 temp = 1 << bit_count;
  if(b)
    {
    data[byte_count] |= temp;
    }
  else
    {
    data[byte_count] &= ~temp;
    }
  }

static bool getbit(const uint8* dataddr, const size_t pos)
  {
  const size_t byte_count = (pos - 1) / 8;
  const size_t bit_count = 7 - ((pos - 1) % 8);
  return (dataddr[byte_count] & (1 << bit_count)) != 0;
  }

static void selectbits(const uint8*   source,
                       const uint8*   table,
                       uint8*         target,
                       const size_t   count)
  {
  for(size_t i = 0; i < count; ++i)
    {
    setbit(target, i + 1, getbit(source, table[i]));
    }
  }

static void shlc(uint8* data)
  {
  const auto b = getbit(data, 1);

  for(size_t i = 0; i < 7; ++i)
    {
    data[i] <<= 1;
    if(i != 6)
      {
      setbit(&data[i], 8, getbit(&data[i + 1], 1));
      }
    }

  setbit(data, 56, getbit(data, 28));
  setbit(data, 28, b);
  }

static void shrc(uint8* data)
  {
  const auto b = getbit(data, 56);

  for(intptr_t i = 6; i >= 0; i--)
    {
    data[i] >>= 1;
    if(i != 0)
      {
      setbit(&data[i], 1, getbit(&data[i - 1], 8));
      }
    }

  setbit(data, 1, getbit(data, 29));
  setbit(data, 29, b);
  }

static void strans(const uint8* index, uint8* target)
  {
  for(size_t i = 0; i < 4; ++i)
    {
    uint8 line = 0;
    setbit(&line, 7, getbit(index, i * 12 + 1));
    setbit(&line, 8, getbit(index, i * 12 + 6));

    uint8 row = 0;
    for(size_t j = 2; j < 6; ++j)
      {
      setbit(&row, 3 + j, getbit(index, i * 12 + j));
      }

    uint8 t = gk_sBox[i * 2][line][row];
    t <<= 4;
    line = row = 0;
    setbit(&line, 7, getbit(index, i * 12 + 7));
    setbit(&line, 8, getbit(index, i * 12 + 12));

    for(size_t j = 2; j < 6; ++j)
      {
      setbit(&row, 3 + j, getbit(index, i * 12 + 6 + j));
      }

    t |= gk_sBox[i * 2 + 1][line][row];
    target[i] = t;
    }
  }

static string DesAlgo(const void*     data,
                      const size_t    size,
                      const DesKey&   key,
                      const bool      enc_or_dec)
  {
  string rets;
  const auto valid_count = size - (size % sizeof(DesKey));
  rets.append((const char*)data, valid_count);

  const uint8* pkey = key.Keys;

  for(size_t i = 0; i < rets.size(); i += sizeof(DesKey))
    {
    uint8* pdata = (uint8*)rets.c_str() + i;
    uint8 tmpdata[12];
    uint8 tmpkey[7];

    selectbits(pdata, gk_IP_TB, tmpdata, 64);
    memcpy(pdata, tmpdata, 8);
    selectbits(pkey, gk_PC1_TB, tmpkey, 56);

    const auto f = enc_or_dec ? shlc : shrc;
    for(size_t i = 0; i < 16; ++i)
      {
      selectbits(pdata + 4, gk_EO_TB, tmpdata, 48);

      const size_t count = enc_or_dec ? i : 16 - i;

      for(size_t j = 0; j < gk_Shift_TB[count]; ++j)
        {
        f(tmpkey);
        }

      selectbits(tmpkey, gk_PC2_TB, tmpdata + 6, 48);
      doxor(tmpdata, tmpdata + 6, 6);
      strans(tmpdata, tmpdata + 6);
      selectbits(tmpdata + 6, gk_PP_TB, tmpdata, 32);
      doxor(tmpdata, pdata, 4);

      if(i < 15)
        {
        memcpy(pdata, pdata + 4, 4);
        memcpy(pdata + 4, tmpdata, 4);
        }
      }

    memcpy(pdata, tmpdata, 4);
    selectbits(pdata, gk_RP_TB, tmpdata, 64);
    memcpy(pdata, tmpdata, 8);
    }

  return rets;
  }

string DesEncrypt(const void* data, const size_t size, const DesKey& key)
  {
  return DesAlgo(data, size, key, true);
  }

string DesDecrypt(const void* data, const size_t size, const DesKey& key)
  {
  return DesAlgo(data, size, key, false);
  }

